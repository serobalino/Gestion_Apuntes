    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
       ventanaPrincipal nueo =new ventanaPrincipal();
       nueo.dispose();
        if (Cedula.isEmpty()) {
            txt_cedula.setEnabled(true);
            
            ArrayList<String> facultades = new ArrayList();
            estudianteDP nuevo = new estudianteDP();
            try {
                facultades=nuevo.Facultades();
                for (int i = 0; i < facultades.size(); i++) {
                    cb_facultad.addItem(facultades.get(i));
                }
                } catch (SQLException ex) {
                Logger.getLogger(VentanaModificarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }
        else
        {
            txt_cedula.setText(Cedula);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_formWindowOpened

    private void cb_facultadItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_facultadItemStateChanged
        String seleccionado = cb_facultad.getSelectedItem().toString();
        ArrayList<String> carreras = new ArrayList();
        estudianteDP nuevo = new estudianteDP();
            try {
                carreras=nuevo.Carreras(seleccionado);
                cb_carrera.removeAllItems();
                for (int i = 0; i < carreras.size(); i++)
                    cb_carrera.addItem(carreras.get(i));
                } catch (SQLException ex) {
                Logger.getLogger(VentanaModificarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
            }    
    }//GEN-LAST:event_cb_facultadItemStateChanged

    private void cb_carreraItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_carreraItemStateChanged
        String seleccionado = cb_carrera.getSelectedItem().toString();
        ArrayList<String> niveles = new ArrayList();
        estudianteDP nuevo = new estudianteDP();
            
            try {
                niveles=nuevo.Niveles(seleccionado);
                cb_nivel.removeAllItems();
                for (int i = 0; i < niveles.size(); i++)
                    cb_nivel.addItem(niveles.get(i));
                } catch (SQLException ex) {
                Logger.getLogger(VentanaModificarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
            }  
    }//GEN-LAST:event_cb_carreraItemStateChanged

